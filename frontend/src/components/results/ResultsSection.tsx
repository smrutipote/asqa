import { motion, AnimatePresence } from 'framer-motion';
import { AGENT_IDS, AGENT_META } from '../../lib/types';
import type { AgentState, AgentId, PipelineStatus } from '../../lib/types';
import { AgentCard } from './AgentCard';
import { RiskAnalysisView } from './RiskAnalysisView';
import { CodeView } from './CodeView';
import { TerminalView } from './TerminalView';
import { BugReportView } from './BugReportView';
import { DiffView } from './DiffView';
import { Clock, CheckCircle2, Wrench, Copy, Check } from 'lucide-react';
import { useState } from 'react';

interface ResultsSectionProps {
  agents: Record<string, AgentState>;
  pipelineStatus: PipelineStatus;
  pipelineSummary: any;
}

function AgentResultContent({ agentId, data }: { agentId: AgentId; data: any }) {
  if (!data) return <p className="text-sm text-gray-500">No data</p>;

  switch (agentId) {
    case 'code_reader':
      return <RiskAnalysisView analysis={data.risk_analysis} />;
    case 'test_generator':
      return <CodeView code={data.generated_test} filename={data.test_filename} />;
    case 'runner':
      return <TerminalView output={data.test_output} exitCode={data.test_exit_code} error={data.execution_error} passed={data.test_passed} />;
    case 'bug_reporter':
      return <BugReportView report={data.bug_report} finalStatus={data.final_status} />;
    case 'fix_suggester':
      return <DiffView patch={data.fix_patch} explanation={data.fix_explanation} />;
    default:
      return <pre className="text-xs">{JSON.stringify(data, null, 2)}</pre>;
  }
}

function SuggestedFixBanner({ agents }: { agents: Record<string, AgentState> }) {
  const [copied, setCopied] = useState(false);
  const fixAgent = agents['fix_suggester'];
  if (!fixAgent || fixAgent.status !== 'completed' || !fixAgent.data) return null;

  const { fix_patch, fix_explanation } = fixAgent.data;
  if (!fix_patch && !fix_explanation) return null;

  const handleCopy = () => {
    navigator.clipboard.writeText(fix_patch || '');
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <motion.div
      initial={{ opacity: 0, y: -10 }}
      animate={{ opacity: 1, y: 0 }}
      className="mb-6 overflow-hidden rounded-2xl border border-primary/30 bg-gradient-to-br from-primary/5 to-blue-900/10"
    >
      <div className="flex items-center justify-between border-b border-primary/20 px-6 py-4">
        <div className="flex items-center gap-3">
          <div className="flex h-10 w-10 items-center justify-center rounded-xl bg-primary/10">
            <Wrench className="h-5 w-5 text-primary" />
          </div>
          <div>
            <h3 className="text-lg font-bold text-white">Suggested Fix</h3>
            <p className="text-xs text-gray-400">Generated by Fix Suggester (Claude Sonnet 4)</p>
          </div>
        </div>
        {fix_patch && (
          <button
            onClick={handleCopy}
            className="inline-flex items-center gap-1.5 rounded-lg border border-gray-700 bg-gray-800 px-3 py-1.5 text-xs font-medium text-gray-300 transition hover:bg-gray-700"
          >
            {copied ? <Check className="h-3.5 w-3.5 text-success" /> : <Copy className="h-3.5 w-3.5" />}
            {copied ? 'Copied!' : 'Copy Patch'}
          </button>
        )}
      </div>

      <div className="p-6">
        {fix_explanation && (
          <div className="mb-4 rounded-lg bg-blue-950/50 p-4">
            <p className="text-xs font-semibold text-blue-400">Explanation</p>
            <p className="mt-1 text-sm leading-relaxed text-blue-300">{fix_explanation}</p>
          </div>
        )}

        {fix_patch && (
          <div className="overflow-auto rounded-lg bg-[#1a1b26] p-4 font-mono text-xs leading-relaxed" style={{ maxHeight: '400px' }}>
            {fix_patch.split('\n').map((line: string, i: number) => (
              <div
                key={i}
                className={
                  line.startsWith('+') && !line.startsWith('+++')
                    ? 'bg-green-900/30 px-2 text-green-400'
                    : line.startsWith('-') && !line.startsWith('---')
                    ? 'bg-red-900/30 px-2 text-red-400'
                    : line.startsWith('@@')
                    ? 'px-2 text-blue-400'
                    : line.startsWith('---')
                    ? 'px-2 text-red-300'
                    : line.startsWith('+++')
                    ? 'px-2 text-green-300'
                    : 'px-2 text-gray-400'
                }
              >
                {line || '\u00A0'}
              </div>
            ))}
          </div>
        )}
      </div>
    </motion.div>
  );
}

export function ResultsSection({ agents, pipelineStatus, pipelineSummary }: ResultsSectionProps) {
  const hasAnyResult = Object.values(agents).some((a) => a.status === 'completed');
  if (!hasAnyResult && pipelineStatus === 'idle') return null;

  return (
    <section className="mx-auto max-w-6xl px-6 pb-20">
      {/* Pipeline completion banner */}
      <AnimatePresence>
        {pipelineSummary && (
          <motion.div
            initial={{ opacity: 0, y: -10 }}
            animate={{ opacity: 1, y: 0 }}
            className="mb-6 flex items-center justify-center gap-6 rounded-xl border border-success/30 bg-success/5 px-6 py-3"
          >
            <div className="flex items-center gap-2 text-success">
              <CheckCircle2 className="h-5 w-5" />
              <span className="text-sm font-semibold">Pipeline Complete</span>
            </div>
            <div className="flex items-center gap-2 text-sm text-gray-400">
              <Clock className="h-4 w-4" />
              {pipelineSummary.mttr_seconds}s
            </div>
            <div className="text-sm font-medium text-gray-300">
              Status: <span className={pipelineSummary.final_status === 'bug_found' ? 'text-error' : 'text-success'}>
                {pipelineSummary.final_status}
              </span>
            </div>
          </motion.div>
        )}
      </AnimatePresence>

      {/* Prominent suggested fix at the top */}
      <SuggestedFixBanner agents={agents} />

      {/* Agent outputs heading */}
      <h3 className="mb-6 text-center text-sm font-semibold uppercase tracking-wider text-gray-500">
        Agent Outputs
      </h3>

      {/* Agent cards — skip agents with errors, skip fix_suggester (shown above) */}
      <div className="space-y-4">
        {AGENT_IDS.map((id) => {
          const agent = agents[id];
          if (!agent || agent.status === 'idle' || agent.status === 'error') return null;
          // Fix Suggester is shown prominently above, skip it here
          if (id === 'fix_suggester' && agent.status === 'completed') return null;

          return (
            <motion.div
              key={id}
              initial={{ opacity: 0, y: 20 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: 0.1 }}
            >
              <AgentCard
                name={AGENT_META[id].name}
                model={AGENT_META[id].model}
                status={agent.status}
                message={agent.message}
                defaultOpen={agent.status === 'completed'}
              >
                <AgentResultContent agentId={id} data={agent.data} />
              </AgentCard>
            </motion.div>
          );
        })}
      </div>
    </section>
  );
}
